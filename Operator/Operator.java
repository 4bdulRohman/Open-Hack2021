package Operator;

public class Operator {

    public static void main(String[] args) {
        int a = 1000;
        int b = 100;
       
        System.out.println("a < b = " + (a<b));
        System.out.println("a > b = " + (a>b));
        System.out.println("a <= b = " + (a<=b));
        System.out.println("a >= b = " + (a>=b));
        System.out.println("a == b = " + (a==b));
        System.out.println("a != b = " + (a!=b));
    }
    
}
